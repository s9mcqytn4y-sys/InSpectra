    fun daftarDefectUntukPart(uniqNo: String): ImmutableList<InputDefect> {
        val materialDefect = daftarRelasiPartMaterial()
            .filter { it.uniqNo == uniqNo }
            .flatMap { it.potensiDefectMaterial }
            .filterNot { it.equals("BELUM DITEMUKAN POTENSI", ignoreCase = true) }
            .distinctBy { it.uppercase() }
            .map {
                InputDefect(
                    idDefect = "MAT_" + it.toKodeDefect(),
                    namaDefect = it,
                    kategori = KategoriDefect.MATERIAL
                )
            }

        val proses = daftarRelasiPartMaterial()
            .firstOrNull { it.uniqNo == uniqNo }
            ?.komoditas

        val defectProses = when (proses) {
            TipeProses.SEWING -> listOf(
                "SEWING MIRING",
                "SEWING LONCAT",
                "SEWING NITIK",
                "KUNCIAN JEBOL",
                "SEWING PUTUS",
                "SEWING LONGGAR",
                "LANGKAH SEWING TIDAK SESUAI"
            )
            TipeProses.PRESS -> listOf(
                "DENT",
                "GALER",
                "HOLE T/A",
                "OVERCUTTING",
                "DIMENSI OUT STD",
                "TERBALIK"
            )
            TipeProses.CUTTING -> listOf(
                "OVERCUTTING",
                "DIMENSI OUT STD",
                "TERBALIK",
                "HOLE T/A",
                "ALUR SERAT TDK SESUAI"
            )
            else -> emptyList()
        }.map {
            InputDefect(
                idDefect = "PROSES_" + it.toKodeDefect(),
                namaDefect = it,
                kategori = KategoriDefect.PROSES
            )
        }

        return (materialDefect + defectProses)
            .distinctBy { it.namaDefect.uppercase() }
            .sortedWith(compareBy<InputDefect> { it.kategori.name }.thenBy { it.namaDefect })
            .toImmutableList()
    }

    fun daftarPartChecksheet(tipeProses: TipeProses): ImmutableList<RingkasanPartChecksheet> {
        val relasi = daftarRelasiPartMaterial()
            .filter { it.komoditas == tipeProses }
            .groupBy { it.uniqNo }

        return relasi.map { (uniqNo, daftarMaterial) ->
            val first = daftarMaterial.first()
            RingkasanPartChecksheet(
                uniqNo = uniqNo,
                nomorPart = first.nomorPart,
                namaPart = first.namaPart,
                komoditas = first.komoditas,
                daftarMaterial = daftarMaterial.toImmutableList(),
                daftarDefect = daftarDefectUntukPart(uniqNo),
                lokasiGambar = daftarPartAcuan().firstOrNull { it.uniqNo == uniqNo }?.lokasiGambar
            )
        }.sortedBy { it.uniqNo }
            .toImmutableList()
    }


    private fun String.toTipeProses(): TipeProses {
        return when (trim().uppercase()) {
            "PRESS" -> TipeProses.PRESS
            "SEWING" -> TipeProses.SEWING
            "CUTTING" -> TipeProses.CUTTING
            "MATERIAL" -> TipeProses.MATERIAL
            "PASSTROUGH", "PASSTHROUGH", "PASS THROUGH", "PASS_THROUGH" -> TipeProses.PASS_THROUGH
            "CONSUMABLE" -> TipeProses.CONSUMABLE
            else -> TipeProses.MATERIAL
        }
    }

    private fun String.splitDefect(): List<String> {
        return split("|")
            .map { it.trim() }
            .filter { it.isNotBlank() }
    }

    private fun String.toKodeDefect(): String {
        return uppercase()
            .replace("/", " ")
            .replace("-", " ")
            .replace("#", " ")
            .replace(".", " ")
            .replace(",", " ")
            .trim()
            .replace(Regex("\\s+"), "_")
    }
